    private boolean ValidarDUI(){
        boolean resp = false;
        if(!txtdui.getText().isEmpty())
    {
        try {
            conexion con = new conexion();
            cn = con.conexion();
            int ID = 0;
            PreparedStatement cmd = cn.prepareStatement("Select * from cliente where DUI = '" + txtdui.getText() + "'");
            ResultSet rs = cmd.executeQuery();
            if(rs.next())
            {
                if(!textid.getText().isEmpty()){
                    ID = rs.getInt(1);
                if(ID != Integer.parseInt(textid.getText()))
                {
                    resp = true;
                    JOptionPane.showMessageDialog(this, "El DUI ingresado ya existe, por favor intente con otro");
                    txtdui.setText(null);
                }
                }
                else
                {
                    resp = true;
                    JOptionPane.showMessageDialog(this, "El DUI ingresado ya existe, por favor intente con otro");
                    txtdui.setText(null);
                }
                
            }
            cmd.close();
            cn.close();
            
            } catch (SQLException ex) {
                ex.getMessage();
            }
    }
        return resp;
    }
    
