    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       // TODO add your handling code here:
        if(!textnombre.getText().isEmpty() && !textapellido.getText().isEmpty() && !txtdui.getText().isEmpty() && !txtnit.getText().isEmpty() && !txttelefono.getText().isEmpty() && !txtCorreo.getText().isEmpty() && !ValidarDUI() && !ValidarNIT() && !ValidarTel() && !ValidarCorreo()){
        mth_cliente obj = new mth_cliente();
        obj.setNombre(textnombre.getText());
        obj.setApellido(textapellido.getText());
        obj.setDUI((txtdui.getText())); 
        obj.setNIT((txtnit.getText()));
        obj.setTelefono((txttelefono.getText()));
        obj.setCorreo(txtCorreo.getText());
        if(obj.guardar())
        {
            actualizar();
            JOptionPane.showMessageDialog(this, "Datos Guardados");
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Error al guardar los datos, asegurece de que no hayan datos repetidos y vuelva a intentarlo");
        }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Por favor llene todos los campos");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

