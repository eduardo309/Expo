    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Consultas obj = new Consultas();
            obj.setMunicipio(txtNombre.getText());
            if(obj.guardardepartamentos()) {
                try {
                    DefaultTableModel modelo=(DefaultTableModel) tabla5.getModel();
                    int filas=tabla5.getRowCount();
                    for (int i = 0;filas>i; i++) {
                        modelo.removeRow(0);
                    }
                    Consultas frm4 = new Consultas();
                    frm4.Tabla5();
                    Consultas.cn.close();
                    txtCodigo.setText("");
        txtNombre.setText("");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Error al limpiar la tabla.");
                }
                JOptionPane.showMessageDialog(this, "Datos guardados");
            }
            else {
                JOptionPane.showMessageDialog(this, "El departamento que ingreso ya existe!!!");
            }
    }//GEN-LAST:event_jButton1ActionPerformed

