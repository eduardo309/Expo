    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        Consultas obj = new Consultas();
        obj.setCodigo(Integer.parseInt(txtCodigo.getText()));
        int eliminar = JOptionPane.showConfirmDialog(this, "¿Está seguro que desea eliminar?", "Atención", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if(eliminar == 0) {
            if(obj.eliminarmunicipios()) {
                try {
            DefaultTableModel modelo=(DefaultTableModel) tabla4.getModel();
            int filas=tabla4.getRowCount();
            for (int i = 0;filas>i; i++) {
                modelo.removeRow(0);
            }
            Consultas frm4 = new Consultas();
            frm4.Tabla4();
            Consultas.cn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al limpiar la tabla.");
        }
                JOptionPane.showMessageDialog(this, "Datos eliminados");
                limpiar();
            }
            else {
                JOptionPane.showMessageDialog(this, "Error al eliminar");
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String path = "";
        try{
            path = getClass().getResource("/Reportes/Municipios.jasper").getPath();
            path = URLDecoder.decode(path, "UTF-8");
            System.out.println(path);
            Connection cn = new conexion().conexion();
            Map parametros = new HashMap();
            JasperReport reporte = (JasperReport)JRLoader.loadObject(path); 
            JasperPrint imprimir = JasperFillManager.fillReport(reporte, parametros, cn); 
            JasperViewer visor = new JasperViewer(imprimir, false);
            visor.setTitle("Reporte de Municipios");
            visor.setVisible(true);
        }
        catch(Exception ex){
            System.out.println(ex);
            System.out.println("Aqui es pendejo");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

