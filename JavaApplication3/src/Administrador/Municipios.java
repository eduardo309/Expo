    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        Consultas obj = new Consultas();
        obj.setMunicipio(txtNombre.getText());
        if(obj.guardarmunicipios()) {
            try {
            DefaultTableModel modelo=(DefaultTableModel) tabla4.getModel();
            int filas=tabla4.getRowCount();
            for (int i = 0;filas>i; i++) {
                modelo.removeRow(0);
            }
            Consultas frm4 = new Consultas();
            frm4.Tabla4();
            Consultas.cn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al limpiar la tabla.");
        }
            JOptionPane.showMessageDialog(this, "Datos guardados");
            limpiar();
        }
        else {
            JOptionPane.showMessageDialog(this, "El municipio que ingreso ya existe!!!");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

